# Programa para calcular el sueldo mensual de un empleado

horasTrabajadas = float(input("Ingrese la cantidad de horas trabajadas:"))
valorHora = float(input("Ingrese el valor de horas trabajadas"))

# Operacion
sueldoMensual = horasTrabajadas * valorHora
print("El sueldo mensual por el total de horas trabajadas es de:" , sueldoMensual)

